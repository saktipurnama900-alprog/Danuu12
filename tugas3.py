# tipe data 

f = 6 # bilangan bulat ( integer "int")
print("nilai dari f :", f)
print(type(f))

i = 2.2 # bilangan pecahan (float "float")
print("nilai i :", i)
print(type(i))

n = "februari" # tipe data string (tulisan "str")
print("nilai dari n :", n)
print(type(n))

a = False # tipe data benar salah (true/false "bool")
print("nilai dari kebenaran : ", a)
print(type(a))